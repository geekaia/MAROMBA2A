    private void btnCalcularIMCActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularIMCActionPerformed
        // TODO add your handling code here:
        String peso = txtPeso.getText();
        String altura = txtAltura.getText();
        try{ 
            Double pesod = Double.parseDouble(peso);
            Double alturad = Double.parseDouble(altura);
            Double imc = pesod / (alturad*alturad); 
            String res="";
            if (imc < 18.5) {
                res="MAGREZA";
            }else  if (imc <= 24.9){
                res="NORMAL";
            } else  if (imc <= 29.9){
                res="SOBREPESO";
            } else  if (imc <= 39.9){
                res="OBESIDADE";
            } else   {
                res="OBESIDADE GRAVE";
            }
              
            lbResultado.setText("Resultado: "+res+" IMC: "+ imc);
        }catch(Exception e) {
            e.printStackTrace();
            lbResultado.setText("Resultado: erro no cálculo");
        }
    }//GEN-LAST:event_btnCalcularIMCActionPerformed

