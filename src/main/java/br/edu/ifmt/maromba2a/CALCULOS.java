    private void btnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularActionPerformed
 
        
        String peso = txtPesoCalorias.getText(); 
        String idade = txtIdade.getValue().toString(); 
        
        String sexo="nulo"; 
        if (btnMasculino.isSelected()) {
            sexo="M"; 
        } else if(btnFeminino.isSelected()) {
            sexo="F"; 
        }
        Double pesod = Double.parseDouble(peso);
        Integer idadei = Integer.parseInt(idade);
        double caloriasBasal=0;
        if (sexo.equals("F")) {
            if (idadei <= 3) {
                caloriasBasal = (58.317 * pesod) - 31.1;
            }  else if (idadei <= 10) {
                caloriasBasal = (20.315 * pesod) + 485.9;
            } 
        } else {
            if (idadei <= 3) { //(59,512 x peso em kg) – 30,4
                caloriasBasal = (59.512 * pesod) - 30.4;
            } else if (idadei <= 10) { //(59,512 x peso em kg) – 30,4
                caloriasBasal = (22.706 * pesod) + 504.3;
            }  // (22,706 
        }
        lbCalRes.setText("Gasto calórico: "+ caloriasBasal);
        
        


            
         
        
    }//GEN-LAST:event_btnCalcularActionPerformed

